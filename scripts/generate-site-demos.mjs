import { readFile, readdir, writeFile } from 'node:fs/promises'
import path from 'node:path'
import process from 'node:process'
import { fileURLToPath } from 'node:url'

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..')
const inventoryPath = path.join(root, 'docs/component-inventory.json')
const examplesRoot = path.join(root, 'playground/examples')
export const registryPath = path.join(root, 'site/demos/registry.tsx')

/**
 * Builds the site's demo registry from the component inventory.
 *
 * The registry maps a conformant component to the playground example that
 * demonstrates it. It is generated rather than hand-maintained so a component
 * cannot be added to the inventory and silently omitted from the site, and so
 * the site never names an example that does not exist.
 */
export async function buildRegistrySource() {
  const inventory = JSON.parse(await readFile(inventoryPath, 'utf8'))
  const conformant = inventory.components
    .filter((component) => component.status === 'conformant')
    .map((component) => component.name)
    .sort((left, right) => left.localeCompare(right))

  const examples = new Set(
    (await readdir(examplesRoot))
      .filter((entry) => entry.endsWith('.example.tsx'))
      .map((entry) => entry.replace('.example.tsx', '')),
  )

  const demonstrated = conformant.filter((name) => examples.has(name))

  const imports = demonstrated
    .map((name) => `import { ${name}Example } from '@examples/${name}.example'`)
    .join('\n')

  const entries = demonstrated.map((name) => `  ${name}: ${name}Example,`).join('\n')

  return `'use client'

// Generated by \`npm run generate:site-demos\` from docs/component-inventory.json.
// Do not edit by hand; \`npm run check:site\` fails when this file is stale.
//
// The examples are imported statically rather than lazily so every demo is
// server-rendered into the exported HTML. That makes the site a real test of
// the package's SSR and client-boundary behavior (ADR 0028) instead of a
// client-only rendering of it.

import type { ComponentType } from 'react'
${imports}

export const demoRegistry: Record<string, ComponentType> = {
${entries}
}

export function getDemo(component: string): ComponentType | undefined {
  return demoRegistry[component]
}
`
}

const invokedDirectly =
  process.argv[1] && path.resolve(process.argv[1]) === fileURLToPath(import.meta.url)

if (invokedDirectly) {
  const source = await buildRegistrySource()
  await writeFile(registryPath, source, 'utf8')
  const count = source.match(/^  \w+: \w+Example,$/gm)?.length ?? 0
  process.stdout.write(`Generated site/demos/registry.tsx with ${count} demos\n`)
}
